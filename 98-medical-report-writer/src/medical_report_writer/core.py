class ReportSession:
    """Track generated reports across a session."""

    def __init__(self) -> None:
        """Initialize the report session."""
        self.reports: List[Dict[str, Any]] = []

    def add_report(
        self,
        report_type: str,
        clinical_data: str,
        generated_report: str,
    ) -> None:
        """Add a generated report to the session.

        Args:
            report_type: Type of report generated.
            clinical_data: Input clinical data.
            generated_report: The generated report text.
        """
        entry = {
            "timestamp": datetime.now().isoformat(),
            "report_type": report_type,
            "clinical_data": clinical_data,
            "generated_report": generated_report,
        }
        self.reports.append(entry)
        logger.info("Added report to session (total: %d)", len(self.reports))

    def get_reports(self) -> List[Dict[str, Any]]:
        """Get all reports in this session.

        Returns:
            Copy of the reports list.
        """
        return list(self.reports)

    def get_summary(self) -> Dict[str, Any]:
        """Get a summary of the session.

        Returns:
            Dictionary with session statistics.
        """
        if not self.reports:
            return {
                "total_reports": 0,
                "report_types": [],
                "all_types": [],
            }

        all_types: List[str] = []
        for entry in self.reports:
            rt = entry["report_type"]
            if rt not in all_types:
                all_types.append(rt)

        return {
            "total_reports": len(self.reports),
            "report_types": all_types,
            "all_types": all_types,
        }


