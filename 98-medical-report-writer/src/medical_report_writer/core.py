def generate_report(
    clinical_data: str,
    report_type: str = "progress_note",
    patient_demographics: Optional[str] = None,
    conversation_history: Optional[List[Dict[str, str]]] = None,
) -> str:
    """Generate a medical report using the LLM.

    Args:
        clinical_data: Free-text clinical data to include in the report.
        report_type: One of the keys in REPORT_TYPES.
        patient_demographics: Optional patient demographic information.
        conversation_history: Optional prior messages for multi-turn context.

    Returns:
        AI-drafted report text.

    Raises:
        ValueError: If report_type is not recognized.
        ConnectionError: If Ollama is not reachable.
    """
    if report_type not in REPORT_TYPES:
        raise ValueError(
            f"Unknown report type '{report_type}'. "
            f"Valid types: {', '.join(REPORT_TYPES.keys())}"
        )

    if conversation_history is None:
        conversation_history = []

    report_info = REPORT_TYPES[report_type]

    prompt_parts = [
        f"Generate a {report_info['name']} based on the following clinical data.",
        f"\nExpected sections: {report_info['sections']}",
    ]

    if patient_demographics:
        prompt_parts.append(f"\nPatient Demographics:\n{patient_demographics}")

    prompt_parts.append(f"\nClinical Data:\n{clinical_data}")
    prompt_parts.append(
        "\nPlease generate a complete, professionally formatted report. "
        "Mark any areas needing physician verification with [PHYSICIAN TO VERIFY]."
    )

    user_message = "\n".join(prompt_parts)

    messages = [{"role": "system", "content": SYSTEM_PROMPT}]
    messages.extend(conversation_history)
    messages.append({"role": "user", "content": user_message})

    logger.info(
        "Generating %s (%d prior messages)",
        report_info["name"], len(conversation_history),
    )

    try:
        response = chat(messages)
        logger.info("Generated report (%d chars)", len(response))
        return response
    except Exception as exc:
        logger.error("Report generation failed: %s", exc)
        raise


def generate_discharge_summary(
    admission_data: str,
    hospital_course: str,
    discharge_info: str,
) -> str:
    """Generate a discharge summary from structured admission data.

    Args:
        admission_data: Admission details (date, diagnosis, presenting complaints).
        hospital_course: Description of hospital stay, treatments, procedures.
        discharge_info: Discharge medications, follow-up, instructions.

    Returns:
        AI-drafted discharge summary.
    """
    clinical_data = (
        f"ADMISSION DATA:\n{admission_data}\n\n"
        f"HOSPITAL COURSE:\n{hospital_course}\n\n"
        f"DISCHARGE INFORMATION:\n{discharge_info}"
    )

    return generate_report(clinical_data, report_type="discharge_summary")


def generate_referral_letter(
    patient_info: str,
    reason: str,
    clinical_findings: str,
    requesting_physician: Optional[str] = None,
) -> str:
    """Generate a referral letter to a specialist.

    Args:
        patient_info: Patient demographics and relevant history.
        reason: Reason for referral.
        clinical_findings: Relevant clinical findings and test results.
        requesting_physician: Name of the referring physician.

    Returns:
        AI-drafted referral letter.
    """
    parts = [
        f"PATIENT INFORMATION:\n{patient_info}",
        f"\nREASON FOR REFERRAL:\n{reason}",
        f"\nCLINICAL FINDINGS:\n{clinical_findings}",
    ]

    if requesting_physician:
        parts.append(f"\nREFERRING PHYSICIAN: {requesting_physician}")

    clinical_data = "\n".join(parts)
    return generate_report(clinical_data, report_type="referral_letter")


def format_report(raw_report: str, formatting_style: str = "standard") -> str:
    """Format a raw report according to the specified style.

    Args:
        raw_report: The raw report text to format.
        formatting_style: One of 'standard', 'compact', 'detailed'.

    Returns:
        Formatted report text.
    """
    if not raw_report or not raw_report.strip():
        return raw_report

    lines = raw_report.strip().split("\n")

    if formatting_style == "compact":
        # Remove extra blank lines
        formatted_lines = []
        prev_blank = False
        for line in lines:
            if not line.strip():
                if not prev_blank:
                    formatted_lines.append("")
                prev_blank = True
            else:
                formatted_lines.append(line)
                prev_blank = False
        return "\n".join(formatted_lines)

    elif formatting_style == "detailed":
        # Add separator lines between sections
        formatted_lines = []
        for line in lines:
            formatted_lines.append(line)
            # Add separator after header-like lines
            stripped = line.strip()
            if stripped and stripped.isupper() and len(stripped) > 3:
                formatted_lines.append("─" * 60)
        return "\n".join(formatted_lines)

    else:
        # Standard formatting — clean up whitespace
        return "\n".join(line.rstrip() for line in lines)


def extract_key_findings(report_text: str) -> List[str]:
    """Extract key findings and important items from a report.

    Looks for lines that indicate findings, diagnoses, or action items.

    Args:
        report_text: The full report text.

    Returns:
        List of key finding strings.
    """
    if not report_text:
        return []

    findings: List[str] = []
    keywords = [
        "diagnosis", "finding", "impression", "assessment",
        "recommendation", "plan", "follow-up", "medication",
        "procedure", "result", "conclusion",
    ]

    for line in report_text.split("\n"):
        stripped = line.strip().lower()
        if not stripped:
            continue
        # Lines starting with bullet points or numbers
        if stripped.startswith(("-", "•", "*")) or (
            len(stripped) > 2 and stripped[0].isdigit() and stripped[1] in ".)"
        ):
            findings.append(line.strip())
        # Lines containing key medical terms
        elif any(kw in stripped for kw in keywords):
            findings.append(line.strip())

    return findings


